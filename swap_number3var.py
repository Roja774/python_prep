"""Write a program to swap two numbers"""
print("Enter two variables:")
var1=int(input())
var2=int(input())
print("Given var1 number is:"+str(var1))
print("Given var2 number is:"+str(var2))
temp=var1
var1=var2
var2=temp
print("After swaping var1 is:"+str(var1))
print("After swaping var2 is:"+str(var2))